def addition (num1,num2):
    answer = num1 + num2
    print (f'{num1} + {num2} = {answer}')
addition(10,12)

def subtraction (num1,num2):
    answer = num1 - num2
    print (f'{num1} - {num2} = {answer}')
subtraction(58,34)

def multiplication (num1,num2):
    answer = num1 * num2
    print (f'{num1} x {num2} = {answer}')
multiplication(6,7)

def division (num1,num2):
    answer = num1 // num2
    print (f'{num1} / {num2} = {answer}')
division(144,12)

def increment (num1):
    inc = 1
    answer = num1 + inc
    print (f'{num1} + {inc} = {answer}')
increment(12)

def decrement (num1):
    inc = 1
    answer = num1 - inc
    print (f'{num1} - {inc} = {answer}')
decrement(34)